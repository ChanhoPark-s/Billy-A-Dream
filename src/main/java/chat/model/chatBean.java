package chat.model;

public class chatBean {
	private int no;
	private int room_no;
	private int send_mno;
	private int read_mno;
	private String time;
	private String content;
	private int pno;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	public int getSend_mno() {
		return send_mno;
	}
	public void setSend_mno(int send_mno) {
		this.send_mno = send_mno;
	}
	public int getRead_mno() {
		return read_mno;
	}
	public void setRead_mno(int read_mno) {
		this.read_mno = read_mno;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	
	
}
